
public class Switch 
{

	public static void main(String[] args) 
	{
		int age = 3;
		
		switch(age)
		{
			case 1:
			{
				System.out.println("hi");
				break;
				
				
			}
		   
			case 2:
			{
				
				System.out.println("hello");
				break;
				
			}
		
			case 4:
			{
				System.out.println("SABBIR");
				break;
			}
			
			
			
			case 3:
			{
				System.out.println("again match");
				break;//here this break is working & stopping the default
			}
			
			default :
			{
				System.out.println("match");
				break;
			}
			
			
			
		}
			
			
			
			
			
		
		
		
		
		
		
		
		
		
		

	}

	
}
